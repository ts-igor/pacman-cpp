#include "PMFood.h"

//----------------------------------------------------------------------------------------------------------------------------------------
PMFood::PMFood(int y, int x) : PMField(y, x) {

	mSymbol = ACS_BULLET;

}
//----------------------------------------------------------------------------------------------------------------------------------------
PMFood::~PMFood() {}
//----------------------------------------------------------------------------------------------------------------------------------------
void PMFood::interact(PMPacman * pm) {

	if (mInteraction) {
		pm->addScore(10);
		pm->eatFood();
		mSymbol = ' ';													// Setting the symbol as an empty field
		mInteraction = false;											// This field does not interact with Pacman no more	
		drawField(stdscr);
	}

}

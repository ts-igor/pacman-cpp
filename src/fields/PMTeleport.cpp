#include "PMTeleport.h"

//----------------------------------------------------------------------------------------------------------------------------------------
PMTeleport::PMTeleport(int y, int x, int tpy, int tpx) : PMField(y, x) {

	mSymbol = ' ';
	mTPY = tpy;
	mTPX = tpx;

}
//----------------------------------------------------------------------------------------------------------------------------------------
PMTeleport::~PMTeleport() {}
//----------------------------------------------------------------------------------------------------------------------------------------
void PMTeleport::interact(PMPacman * pm) {

	pm->changeCoordinates(mTPY, mTPX);

}

#include "PMWall.h"

//----------------------------------------------------------------------------------------------------------------------------------------
PMWall::PMWall(int y, int x) : PMField(y, x) {

	mSymbol = ACS_CKBOARD;
	init_pair(10, COLOR_BLUE, COLOR_BLUE);

}
//----------------------------------------------------------------------------------------------------------------------------------------
PMWall::~PMWall() {}
//----------------------------------------------------------------------------------------------------------------------------------------
void PMWall::drawField(WINDOW * w) const {

	attron(COLOR_PAIR(10) | A_BOLD);
	mvwaddch(w, posY + Y_SHIFT, posX + X_SHIFT, mSymbol);
	attroff(COLOR_PAIR(10) | A_BOLD);

}

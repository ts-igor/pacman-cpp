#include "PMField.h"

//----------------------------------------------------------------------------------------------------------------------------------------
PMField::PMField(int y, int x) {

	mSymbol = ' ';
	posX = x;
	posY = y;
	mInteraction = true;
	
}
//----------------------------------------------------------------------------------------------------------------------------------------
PMField::~PMField() {}
//----------------------------------------------------------------------------------------------------------------------------------------
void PMField::drawField(WINDOW * win) const {

	attron(A_BOLD);
	mvwaddch(win, posY + Y_SHIFT, posX + X_SHIFT, mSymbol);
	attroff(A_BOLD);
	
}
//----------------------------------------------------------------------------------------------------------------------------------------
bool PMField::accessField() const {

	return true;
	
}
